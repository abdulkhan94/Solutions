void parting(int a, int* array, int counter)
{
  //Variables
  int i=0;
  int value= 0;
  
  //Executable Statements
  if(a != 0)
    {
      for ( value = 1; value <= a; value = value+1 )
	{
	  array[counter] = value;
	  parting( a - value, array, counter+1);
	}
    }
  else if( a == 0 )
    {
      printf("= ");
      for( i = 0; i < counter - 1; i = i+1)
	{
	  printf("%d + ", array[i]);
	}
      printf("%d\n",array[counter-1]);
    }

}

